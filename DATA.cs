        public void ExtractDATA(string inputFile, string outputDirectory)
        {
            FileStream fs1 = new FileStream(inputFile, FileMode.Open, FileAccess.Read);
            BinaryReader br = new BinaryReader(fs1);
            Console.WriteLine("File has been read.");
            uint offsetStart = br.ReadUInt32() * 2048;
            uint numberOfFiles = 5874;
            uint lastOffset = offsetStart;

            if (!Directory.Exists(outputDirectory)) Directory.CreateDirectory(outputDirectory);

            TableofContents[] toc = new TableofContents[numberOfFiles];

            for (int n = 0; n < numberOfFiles; n++)
            {
                toc[n].fileLength = br.ReadUInt32() * 2048;
                toc[n].fileOffset = lastOffset;
                toc[n].calcLength = toc[n].fileLength - toc[n].fileOffset;

                lastOffset = toc[n].fileLength;

                Console.CursorLeft = 0;
                Console.Write($"Reading Files {n}/{numberOfFiles - 1}");
            }

            Console.WriteLine();

            string[] fileName = new string[numberOfFiles];

            for (int n = 0; n < numberOfFiles; n++)
            {
                fileName[n] = (n).ToString("00000000", CultureInfo.InvariantCulture);
            }

            for (int n = 0; n < nicenames.Length; n++)
            {
                fileName[n] = nicenames[n];
            }

            string[] filelist = new string[numberOfFiles];

            for (int n = 0; n < numberOfFiles; n++)
            {
                if (toc[n].fileLength == 0)
                {
                    Console.WriteLine($"Warning: File \"{n}\" is a null file; Skipping.");

                    filelist[n] = NULL_FILE;

                    continue;
                }

                byte[] fileheader = new byte[4];
                fs1.Seek(toc[n].fileOffset, SeekOrigin.Begin);
                fs1.Read(fileheader, 0, 4);
                byte[] filedata = new byte[toc[n].calcLength];
                fs1.Seek(toc[n].fileOffset, SeekOrigin.Begin);
                fs1.Read(filedata, 0, filedata.Length);

                string ext = GetExt(fileheader);

                string outputFile = Path.Combine(outputDirectory, fileName[n] + ext);
                if (File.Exists(outputFile))
                {
                    Console.WriteLine($"Warning: File \"{outputFile}\" already exists. Overwriting.");
                }
                File.WriteAllBytes(outputFile, filedata);

                filelist[n] = outputFile;
            }
        }

        public string GetExt(byte[] buffer)
        {
            if (buffer.SequenceEqual(png))
            {
                return ".png";
            }
            else if (buffer.SequenceEqual(gif))
            {
                return ".gif";
            }
            else if (buffer.SequenceEqual(tmh))
            {
                return ".tmh";
            }
            else if (buffer.SequenceEqual(gim))
            {
                return ".gim";
            }
            else if (buffer.SequenceEqual(mwo))
            {
                return ".ovl";
            }
            else if (buffer.SequenceEqual(head))
            {
                return ".head";
            }
            else if (buffer.SequenceEqual(dbst))
            {
                return ".dbst";
            }
            else if (buffer.SequenceEqual(wav))
            {
                return ".wav";
            }
            else if (buffer.SequenceEqual(mib))
            {
                return ".mib";
            }
            else if (buffer.SequenceEqual(psmf))
            {
                return ".pmf";
            }
            else if (buffer.SequenceEqual(p1mod))
            {
                return ".p1mod";
            }
            else return ".bin";
        }

